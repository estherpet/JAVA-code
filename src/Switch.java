public class Switch {
    public static void main(String[] args){
        int number = 5;
        switch (number){
            case 3:
                System.out.println("number is 3");
                break;
            case 4:
                System.out.println("number is 4");
                break;
            case 5:
                System.out.println("number is 5");
                break;
            default:
                System.out.println("no valid number");

        }
    }
}
