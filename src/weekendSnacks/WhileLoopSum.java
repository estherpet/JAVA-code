package weekendSnacks;

public class WhileLoopSum {
    public static void main(String[] args) {
        int [] array = {23,54,46,88,99,76,90};
        int sum = 0;
        int i = 0;
        while (i < array. length) {
            sum += array[i];
            i+= 1;
        }
            System.out.println(sum);
        }
    }

